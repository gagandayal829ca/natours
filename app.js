/** Below is another way of doing the above, we can chain methods like this for tours */
app.route('/api/v1/tours').get(getAllTours).post(createTour);

app
  .route('/api/v1/tours/:id')
  .get(getTour)
  .patch(updateTour)
  .delete(deleteTour);

// 4. Server
app.listen(PORT, () => {
  console.log(`Server started at PORT: ${PORT}`);
});
